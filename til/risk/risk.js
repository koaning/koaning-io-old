var width  = 800,
    height = 400;

var color = d3.scale.category20();

var force = d3.layout.force()
    .charge(-150)
    .linkDistance(30)
    .size([width, height]);

var svg = div.append("svg")
    .attr("width", width)
    .attr("height", height);

force
    .nodes(graph.nodes)
    .links(graph.links)
    .start();

var link = svg.selectAll("line.link")
    .data(graph.links)
    .enter().append("line")
    .attr("class", "link")
    .style("stroke-width", 2)
    .style("stroke", "#999")
    .style("stroke-opacity", 0.6);

var node = svg.selectAll("circle.node")
    .data(graph.nodes)
    .enter().append("circle")
    .attr("class", "node")
    .attr("r", 6)
    .attr("stroke", "#fff")
    .attr("stroke-width", 2)
    .style("fill", function(d) {  switch (d.group){
      case "Australia":
        return "purple"
        break;
      case "Afrika":
        return "brown"
        break; 
      case "TheEast":
        return "green"
        break; 
      case "EUR":
        return "blue"
        break; 
      case "NA":
        return "yellow"
        break; 
      case "SA":
        return "red"
        break; 
      }})
    .call(force.drag);

node.append("title")
    .text(function(d) { return d.name; });

force.on("tick", function() {
  link.attr("x1", function(d) { return d.source.x; })
      .attr("y1", function(d) { return d.source.y; })
      .attr("x2", function(d) { return d.target.x; })
      .attr("y2", function(d) { return d.target.y; });

  node.attr("cx", function(d) { return d.x; })
      .attr("cy", function(d) { return d.y; });
});